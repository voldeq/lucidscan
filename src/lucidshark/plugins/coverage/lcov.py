"""Lcov coverage plugin.

Parses lcov .info files generated by gcov/lcov for C++ code coverage.
These files are typically produced by compiling with --coverage flags
and running lcov after test execution.
https://github.com/linux-test-project/lcov
"""

from __future__ import annotations

from pathlib import Path
from typing import Dict, List, Optional

from lucidshark.core.logging import get_logger
from lucidshark.core.models import ScanContext
from lucidshark.plugins.coverage.base import (
    CoveragePlugin,
    CoverageResult,
    FileCoverage,
)
from lucidshark.plugins.cpp_utils import find_lcov, get_tool_version

LOGGER = get_logger(__name__)


class LcovPlugin(CoveragePlugin):
    """Lcov plugin for C++ code coverage analysis."""

    def __init__(self, project_root: Optional[Path] = None, **kwargs) -> None:
        self._project_root = project_root

    @property
    def name(self) -> str:
        return "lcov"

    @property
    def languages(self) -> List[str]:
        return ["c++"]

    def get_version(self) -> str:
        return get_tool_version(find_lcov)

    def ensure_binary(self) -> Path:
        return find_lcov()

    def measure_coverage(
        self,
        context: ScanContext,
        threshold: float = 80.0,
    ) -> CoverageResult:
        """Parse existing lcov coverage data from .info files.

        Looks for coverage.info in the project root and build directory.

        Args:
            context: Scan context with paths and configuration.
            threshold: Coverage percentage threshold (default 80%).

        Returns:
            CoverageResult with coverage statistics.
        """
        # Find coverage.info file
        coverage_file = self._find_coverage_file(context.project_root)
        if not coverage_file:
            LOGGER.warning("No lcov coverage.info file found")
            result = CoverageResult(threshold=threshold, tool="lcov")
            result.issues.append(self._create_no_data_issue())
            return result

        LOGGER.info(f"Parsing lcov coverage data from {coverage_file}...")

        result = self._parse_lcov_info(coverage_file, context.project_root, threshold)

        if result.total_lines == 0 and not result.issues:
            result.issues.append(self._create_no_data_issue())

        return result

    def _find_coverage_file(self, project_root: Path) -> Optional[Path]:
        """Find the lcov coverage.info file.

        Checks several common locations in order of preference.

        Args:
            project_root: Project root directory.

        Returns:
            Path to coverage.info or None.
        """
        candidates = [
            project_root / "coverage.info",
            project_root / "lcov.info",
            project_root / "build" / "coverage.info",
            project_root / "build" / "lcov.info",
            project_root / "cmake-build-debug" / "coverage.info",
            project_root / "cmake-build-release" / "coverage.info",
        ]

        for candidate in candidates:
            if candidate.exists():
                return candidate

        return None

    def _parse_lcov_info(
        self,
        coverage_file: Path,
        project_root: Path,
        threshold: float,
    ) -> CoverageResult:
        """Parse lcov .info file format.

        The lcov info format uses records like:
            SF:/path/to/source.cpp
            DA:line_number,execution_count
            LH:lines_hit
            LF:lines_found
            end_of_record

        Args:
            coverage_file: Path to the .info file.
            project_root: Project root directory.
            threshold: Coverage percentage threshold.

        Returns:
            CoverageResult with parsed data.
        """
        result = CoverageResult(threshold=threshold, tool="lcov")

        try:
            content = coverage_file.read_text(encoding="utf-8", errors="replace")
        except OSError as e:
            LOGGER.error(f"Failed to read coverage file: {e}")
            return result

        total_lines_found = 0
        total_lines_hit = 0

        current_file = ""
        file_lines_found = 0
        file_lines_hit = 0
        file_missing_lines: List[int] = []
        file_hit_lines: Dict[int, int] = {}

        for line in content.splitlines():
            line = line.strip()

            if line.startswith("SF:"):
                # Start of a new source file record
                current_file = line[3:]
                file_lines_found = 0
                file_lines_hit = 0
                file_missing_lines = []
                file_hit_lines = {}

            elif line.startswith("DA:"):
                # Data line: DA:line_number,execution_count[,checksum]
                parts = line[3:].split(",")
                if len(parts) >= 2:
                    try:
                        line_num = int(parts[0])
                        count = int(parts[1])
                        file_hit_lines[line_num] = count
                        file_lines_found += 1
                        if count > 0:
                            file_lines_hit += 1
                        else:
                            file_missing_lines.append(line_num)
                    except ValueError:
                        pass

            elif line.startswith("LF:"):
                # Lines found (total instrumented lines)
                try:
                    file_lines_found = int(line[3:])
                except ValueError:
                    pass

            elif line.startswith("LH:"):
                # Lines hit (covered lines)
                try:
                    file_lines_hit = int(line[3:])
                except ValueError:
                    pass

            elif line == "end_of_record":
                # End of a file record - store the data
                if current_file:
                    # Make path relative to project root
                    rel_path = self._make_relative(current_file, project_root)

                    file_coverage = FileCoverage(
                        file_path=project_root / rel_path,
                        total_lines=file_lines_found,
                        covered_lines=file_lines_hit,
                        missing_lines=sorted(file_missing_lines),
                    )
                    result.files[rel_path] = file_coverage

                    total_lines_found += file_lines_found
                    total_lines_hit += file_lines_hit

                # Reset for next file
                current_file = ""
                file_missing_lines = []
                file_hit_lines = {}

        result.total_lines = total_lines_found
        result.covered_lines = total_lines_hit
        result.missing_lines = total_lines_found - total_lines_hit

        # Generate issue if below threshold
        percentage = result.percentage
        if percentage < threshold:
            issue = self._create_coverage_issue(
                percentage, threshold, total_lines_found, total_lines_hit
            )
            result.issues.append(issue)

        LOGGER.info(
            f"lcov coverage: {percentage:.1f}% ({total_lines_hit}/{total_lines_found} lines) "
            f"- threshold: {threshold}%"
        )

        return result

    def _make_relative(self, file_path: str, project_root: Path) -> str:
        """Make a file path relative to the project root.

        Args:
            file_path: Absolute or relative file path from lcov.
            project_root: Project root directory.

        Returns:
            Relative path string.
        """
        try:
            p = Path(file_path)
            resolved_root = project_root.resolve()
            if p.is_absolute():
                resolved_file = p.resolve()
                return str(resolved_file.relative_to(resolved_root))
        except (ValueError, OSError):
            pass

        return file_path
