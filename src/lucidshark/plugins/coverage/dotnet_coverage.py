"""dotnet coverage plugin.

Parses Cobertura XML coverage reports produced by
`dotnet test --collect:"XPlat Code Coverage"`.
https://learn.microsoft.com/en-us/dotnet/core/testing/unit-testing-code-coverage
"""

from __future__ import annotations

import shutil
from pathlib import Path

import defusedxml.ElementTree as ET  # type: ignore[import-untyped]
from typing import List, Optional

from lucidshark.core.logging import get_logger
from lucidshark.core.models import ScanContext
from lucidshark.plugins.coverage.base import (
    CoveragePlugin,
    CoverageResult,
    FileCoverage,
)

LOGGER = get_logger(__name__)


def _find_dotnet() -> Path:
    """Find the dotnet CLI binary.

    Returns:
        Path to dotnet binary.

    Raises:
        FileNotFoundError: If dotnet is not installed.
    """
    dotnet = shutil.which("dotnet")
    if dotnet:
        return Path(dotnet)

    raise FileNotFoundError(
        "dotnet is not installed. Install the .NET SDK from:\n"
        "  https://dotnet.microsoft.com/download"
    )


def _find_project_file(project_root: Path) -> Optional[Path]:
    """Find a .sln or .csproj file in the project root.

    Args:
        project_root: Project root directory.

    Returns:
        Path to the project/solution file, or None.
    """
    sln_files = list(project_root.glob("*.sln"))
    if sln_files:
        return sln_files[0]

    csproj_files = list(project_root.glob("*.csproj"))
    if csproj_files:
        return csproj_files[0]

    csproj_files = list(project_root.glob("*/*.csproj"))
    if csproj_files:
        return csproj_files[0].parent

    return None


class DotnetCoveragePlugin(CoveragePlugin):
    """dotnet coverage plugin for C# code coverage analysis.

    Parses Cobertura XML reports generated by XPlat Code Coverage
    collector via `dotnet test --collect:"XPlat Code Coverage"`.
    """

    @property
    def name(self) -> str:
        """Plugin identifier."""
        return "dotnet_coverage"

    @property
    def languages(self) -> List[str]:
        """Supported languages."""
        return ["csharp"]

    def get_version(self) -> str:
        """Get dotnet SDK version."""
        try:
            from lucidshark.plugins.utils import get_cli_version

            binary = self.ensure_binary()
            return get_cli_version(binary)
        except FileNotFoundError:
            return "unknown"

    def ensure_binary(self) -> Path:
        """Ensure dotnet CLI is available.

        Returns:
            Path to dotnet binary.

        Raises:
            FileNotFoundError: If dotnet is not installed.
        """
        return _find_dotnet()

    def measure_coverage(
        self,
        context: ScanContext,
        threshold: float = 80.0,
    ) -> CoverageResult:
        """Parse existing Cobertura XML coverage report.

        Looks for Cobertura XML in the TestResults directory
        (produced by dotnet test --collect:"XPlat Code Coverage").

        Args:
            context: Scan context with paths and configuration.
            threshold: Coverage percentage threshold (default 80%).

        Returns:
            CoverageResult with coverage statistics.
        """
        project_file = _find_project_file(context.project_root)
        if not project_file:
            LOGGER.info("No .sln or .csproj found, skipping dotnet coverage")
            return CoverageResult(threshold=threshold, tool="dotnet_coverage")

        # Find Cobertura XML report
        report_path = self._find_cobertura_report(context.project_root)
        if not report_path:
            LOGGER.warning("No Cobertura coverage report found")
            result = CoverageResult(threshold=threshold, tool="dotnet_coverage")
            result.issues.append(self._create_no_data_issue())
            return result

        LOGGER.info(f"Using Cobertura report: {report_path}")

        result = self._parse_cobertura_report(
            report_path, context.project_root, threshold
        )

        if result.total_lines == 0 and not result.issues:
            result.issues.append(self._create_no_data_issue())

        return result

    def _find_cobertura_report(self, project_root: Path) -> Optional[Path]:
        """Find the most recent Cobertura XML coverage report.

        Searches in TestResults directory for coverage.cobertura.xml files.

        Args:
            project_root: Project root directory.

        Returns:
            Path to the report, or None.
        """
        results_dir = project_root / "TestResults"
        if not results_dir.exists():
            return None

        # Find all Cobertura XML files
        cobertura_files = sorted(
            results_dir.glob("**/coverage.cobertura.xml"),
            key=lambda p: p.stat().st_mtime,
            reverse=True,
        )

        if cobertura_files:
            return cobertura_files[0]

        return None

    def _parse_cobertura_report(
        self,
        report_path: Path,
        project_root: Path,
        threshold: float,
    ) -> CoverageResult:
        """Parse a Cobertura XML coverage report.

        Args:
            report_path: Path to Cobertura XML report.
            project_root: Project root directory.
            threshold: Coverage percentage threshold.

        Returns:
            CoverageResult with parsed data.
        """
        try:
            tree = ET.parse(report_path)
            root = tree.getroot()
            assert root is not None
        except Exception as e:
            LOGGER.error(f"Failed to parse Cobertura report: {e}")
            return CoverageResult(threshold=threshold, tool="dotnet_coverage")

        # Extract overall stats from <coverage> element
        line_rate = float(root.get("line-rate", "0"))
        lines_valid = int(root.get("lines-valid", "0"))
        lines_covered = int(root.get("lines-covered", "0"))

        result = CoverageResult(
            total_lines=lines_valid,
            covered_lines=lines_covered,
            missing_lines=lines_valid - lines_covered,
            threshold=threshold,
            tool="dotnet_coverage",
        )

        # Parse per-file coverage from <class> elements
        for class_elem in root.iter("class"):
            filename = class_elem.get("filename", "")
            if not filename:
                continue

            class_line_rate = float(class_elem.get("line-rate", "0"))
            lines = class_elem.findall(".//line")
            file_total = len(lines)
            file_covered = sum(1 for l in lines if int(l.get("hits", "0")) > 0)

            # Collect missing lines
            missing_lines = [
                int(l.get("number", "0"))
                for l in lines
                if int(l.get("hits", "0")) == 0
            ]

            file_path = Path(filename)
            if not file_path.is_absolute():
                file_path = project_root / file_path

            try:
                rel_path = str(file_path.relative_to(project_root))
            except ValueError:
                rel_path = filename

            file_coverage = FileCoverage(
                file_path=file_path,
                total_lines=file_total,
                covered_lines=file_covered,
                missing_lines=sorted(missing_lines),
            )
            result.files[rel_path] = file_coverage

        # Generate issue if below threshold
        percentage = result.percentage
        if percentage < threshold:
            issue = self._create_coverage_issue(
                percentage, threshold, lines_valid, lines_covered
            )
            result.issues.append(issue)

        LOGGER.info(
            f"dotnet coverage: {percentage:.1f}% ({lines_covered}/{lines_valid} lines) "
            f"- threshold: {threshold}%"
        )

        return result
