"""Swift coverage plugin.

Parses Swift code coverage data from llvm-cov JSON export format,
generated by `swift test --enable-code-coverage`.
"""

from __future__ import annotations

import json
import subprocess
from pathlib import Path
from typing import List, Optional

from lucidshark.core.logging import get_logger
from lucidshark.core.models import ScanContext
from lucidshark.plugins.coverage.base import (
    CoveragePlugin,
    CoverageResult,
    FileCoverage,
)
from lucidshark.plugins.swift_utils import (
    find_swift,
    get_swift_version,
    has_package_swift,
)

LOGGER = get_logger(__name__)


class SwiftCoveragePlugin(CoveragePlugin):
    """Swift coverage plugin using llvm-cov for code coverage analysis."""

    def __init__(self, project_root: Optional[Path] = None, **kwargs) -> None:
        self._project_root = project_root

    @property
    def name(self) -> str:
        return "swift_coverage"

    @property
    def languages(self) -> List[str]:
        return ["swift"]

    def get_version(self) -> str:
        return get_swift_version()

    def ensure_binary(self) -> Path:
        return find_swift()

    def measure_coverage(
        self,
        context: ScanContext,
        threshold: float = 80.0,
    ) -> CoverageResult:
        """Parse existing Swift coverage data from llvm-cov.

        Looks for the coverage profdata in .build/debug/codecov/ which is
        generated by `swift test --enable-code-coverage`.
        """
        if not has_package_swift(context.project_root):
            LOGGER.info("No Package.swift found, skipping swift coverage")
            return CoverageResult(threshold=threshold, tool="swift_coverage")

        # Try to find and export coverage data
        coverage_data = self._export_coverage(context)
        if coverage_data is None:
            LOGGER.warning("No Swift coverage data found")
            result = CoverageResult(threshold=threshold, tool="swift_coverage")
            result.issues.append(self._create_no_data_issue())
            return result

        result = self._parse_llvm_cov_export(
            coverage_data, context.project_root, threshold
        )

        if result.total_lines == 0 and not result.issues:
            result.issues.append(self._create_no_data_issue())

        return result

    def _export_coverage(self, context: ScanContext) -> Optional[dict]:
        """Export coverage data using llvm-cov.

        swift test --enable-code-coverage produces profdata files.
        We use `swift test --show-codecov-path` to find the JSON coverage report,
        or fall back to llvm-cov export.
        """
        try:
            swift_bin = self.ensure_binary()
        except FileNotFoundError:
            return None

        # Use swift test --show-codecov-path to get the coverage JSON path
        try:
            result = subprocess.run(
                [str(swift_bin), "test", "--show-codecov-path"],
                cwd=context.project_root,
                capture_output=True,
                text=True,
                timeout=30,
            )
            if result.returncode == 0 and result.stdout.strip():
                codecov_path = Path(result.stdout.strip())
                if codecov_path.exists():
                    LOGGER.info(f"Using Swift codecov data from {codecov_path}")
                    try:
                        return json.loads(codecov_path.read_text())
                    except (json.JSONDecodeError, OSError) as e:
                        LOGGER.warning(f"Failed to parse codecov JSON: {e}")
        except (subprocess.TimeoutExpired, Exception) as e:
            LOGGER.debug(f"swift test --show-codecov-path failed: {e}")

        return None

    def _parse_llvm_cov_export(
        self,
        data: dict,
        project_root: Path,
        threshold: float,
    ) -> CoverageResult:
        """Parse llvm-cov JSON export format.

        The codecov JSON format has:
        {
            "data": [{
                "totals": {"lines": {"count": N, "covered": M, "percent": P}},
                "files": [{
                    "filename": "...",
                    "summary": {"lines": {"count": N, "covered": M, "percent": P}},
                    "segments": [...]
                }]
            }]
        }
        """
        result = CoverageResult(threshold=threshold, tool="swift_coverage")

        coverage_data = data.get("data", [])
        if not coverage_data:
            return result

        # Use the first (and usually only) data entry
        entry = coverage_data[0] if coverage_data else {}
        totals = entry.get("totals", {})
        files = entry.get("files", [])

        lines_data = totals.get("lines", {})
        total_lines = lines_data.get("count", 0)
        covered_lines = lines_data.get("covered", 0)

        result.total_lines = total_lines
        result.covered_lines = covered_lines
        result.missing_lines = total_lines - covered_lines

        # Parse per-file coverage
        for file_entry in files:
            filename = file_entry.get("filename", "")
            if not filename:
                continue

            file_summary = file_entry.get("summary", {})
            file_lines = file_summary.get("lines", {})
            file_total = file_lines.get("count", 0)
            file_covered = file_lines.get("covered", 0)

            # Find missing lines from segments
            missing_lines = self._extract_missing_lines(file_entry)

            file_path = Path(filename)
            if not file_path.is_absolute():
                file_path = project_root / file_path

            # Make relative for the files dict key
            try:
                rel_path = str(file_path.relative_to(project_root))
            except ValueError:
                rel_path = filename

            file_coverage = FileCoverage(
                file_path=file_path,
                total_lines=file_total,
                covered_lines=file_covered,
                missing_lines=missing_lines,
            )
            result.files[rel_path] = file_coverage

        # Generate issue if below threshold
        percentage = result.percentage
        if percentage < threshold:
            issue = self._create_coverage_issue(
                percentage, threshold, total_lines, covered_lines
            )
            result.issues.append(issue)

        LOGGER.info(
            f"Swift coverage: {percentage:.1f}% ({covered_lines}/{total_lines} lines) "
            f"- threshold: {threshold}%"
        )

        return result

    def _extract_missing_lines(self, file_entry: dict) -> List[int]:
        """Extract missing (uncovered) line numbers from file segments.

        Segments format: [[line, col, count, hasCount, isRegionEntry], ...]
        """
        missing = []
        segments = file_entry.get("segments", [])

        for segment in segments:
            if len(segment) >= 3:
                line = segment[0]
                count = segment[2]
                if count == 0 and line > 0:
                    missing.append(line)

        return sorted(set(missing))
