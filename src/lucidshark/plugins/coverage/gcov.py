"""gcov/lcov coverage plugin.

Parses C coverage data from lcov info files or gcov .gcno/.gcda files
generated by compiling with --coverage and running tests.
"""

from __future__ import annotations

from pathlib import Path
from typing import Dict, List, Optional

from lucidshark.core.logging import get_logger
from lucidshark.core.models import ScanContext
from lucidshark.plugins.c_utils import find_gcov, get_gcov_version
from lucidshark.plugins.coverage.base import (
    CoveragePlugin,
    CoverageResult,
    FileCoverage,
)

LOGGER = get_logger(__name__)


class GcovPlugin(CoveragePlugin):
    """gcov/lcov plugin for C code coverage analysis."""

    def __init__(self, project_root: Optional[Path] = None, **kwargs) -> None:
        self._project_root = project_root

    @property
    def name(self) -> str:
        return "gcov"

    @property
    def languages(self) -> List[str]:
        return ["c"]

    def get_version(self) -> str:
        return get_gcov_version()

    def ensure_binary(self) -> Path:
        return find_gcov()

    def measure_coverage(
        self,
        context: ScanContext,
        threshold: float = 80.0,
    ) -> CoverageResult:
        """Parse existing coverage data.

        Looks for lcov info files first (coverage.info, lcov.info),
        then falls back to searching for .gcda files.

        Args:
            context: Scan context with paths and configuration.
            threshold: Coverage percentage threshold (default 80%).

        Returns:
            CoverageResult with coverage statistics.
        """
        # Try lcov info file first (most common in CI)
        for info_name in ("coverage.info", "lcov.info"):
            info_file = context.project_root / info_name
            if info_file.exists():
                LOGGER.info(f"Parsing C coverage data from {info_name}...")
                return self._parse_lcov_info(info_file, context.project_root, threshold)

        # Also check common build directories
        from lucidshark.plugins.c_utils import has_build_dir

        build_dir = has_build_dir(context.project_root)
        if build_dir:
            for info_name in ("coverage.info", "lcov.info"):
                info_file = build_dir / info_name
                if info_file.exists():
                    LOGGER.info(f"Parsing C coverage data from {build_dir.name}/{info_name}...")
                    return self._parse_lcov_info(
                        info_file, context.project_root, threshold
                    )

        # No coverage data found
        LOGGER.warning("No coverage data found (coverage.info or lcov.info)")
        result = CoverageResult(threshold=threshold, tool="gcov")
        result.issues.append(self._create_no_data_issue())
        return result

    def _parse_lcov_info(
        self,
        info_file: Path,
        project_root: Path,
        threshold: float,
    ) -> CoverageResult:
        """Parse lcov info format.

        The lcov info format contains records like:
            SF:/path/to/file.c     (source file)
            DA:10,1                (line 10, hit 1 time)
            DA:11,0                (line 11, not hit)
            LF:100                 (total lines)
            LH:80                  (hit lines)
            end_of_record

        Args:
            info_file: Path to lcov info file.
            project_root: Project root directory.
            threshold: Coverage percentage threshold.

        Returns:
            CoverageResult with parsed data.
        """
        result = CoverageResult(threshold=threshold, tool="gcov")

        try:
            content = info_file.read_text(encoding="utf-8", errors="replace")
        except OSError as e:
            LOGGER.error(f"Failed to read coverage info: {e}")
            return result

        current_file: Optional[str] = None
        file_lines: Dict[str, Dict[int, int]] = {}  # file -> {line: hit_count}

        for line in content.splitlines():
            line = line.strip()

            if line.startswith("SF:"):
                current_file = line[3:]
            elif line.startswith("DA:") and current_file:
                parts = line[3:].split(",")
                if len(parts) >= 2:
                    try:
                        line_num = int(parts[0])
                        hit_count = int(parts[1])
                        if current_file not in file_lines:
                            file_lines[current_file] = {}
                        file_lines[current_file][line_num] = hit_count
                    except ValueError:
                        pass
            elif line == "end_of_record":
                current_file = None

        # Compute per-file and overall coverage
        total_lines = 0
        total_covered = 0

        for file_path_str, lines in file_lines.items():
            file_total = len(lines)
            file_covered = sum(1 for count in lines.values() if count > 0)
            missing = sorted(ln for ln, count in lines.items() if count == 0)

            total_lines += file_total
            total_covered += file_covered

            # Make relative path
            try:
                rel_path = str(Path(file_path_str).relative_to(project_root))
            except ValueError:
                rel_path = file_path_str

            file_coverage = FileCoverage(
                file_path=project_root / rel_path,
                total_lines=file_total,
                covered_lines=file_covered,
                missing_lines=missing,
            )
            result.files[rel_path] = file_coverage

        result.total_lines = total_lines
        result.covered_lines = total_covered
        result.missing_lines = total_lines - total_covered

        # Generate issue if below threshold
        percentage = result.percentage
        if percentage < threshold:
            issue = self._create_coverage_issue(
                percentage, threshold, total_lines, total_covered
            )
            result.issues.append(issue)

        LOGGER.info(
            f"gcov coverage: {percentage:.1f}% ({total_covered}/{total_lines} lines) "
            f"- threshold: {threshold}%"
        )

        return result
