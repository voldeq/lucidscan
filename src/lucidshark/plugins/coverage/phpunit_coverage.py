"""PHPUnit Clover coverage plugin.

Parses Clover XML coverage data generated by PHPUnit with
--coverage-clover flag (requires Xdebug or PCOV PHP extension).
"""

from __future__ import annotations

import defusedxml.ElementTree as ET  # type: ignore[import-untyped]
from pathlib import Path
from typing import Any, List, Optional

from lucidshark.core.logging import get_logger
from lucidshark.core.models import ScanContext
from lucidshark.plugins.coverage.base import (
    CoveragePlugin,
    CoverageResult,
    FileCoverage,
)

LOGGER = get_logger(__name__)

# Standard paths where PHPUnit coverage-clover output is found
CLOVER_PATHS = [
    "coverage-clover.xml",
    "coverage.xml",
    "build/logs/clover.xml",
    "clover.xml",
]


class PhpunitCoveragePlugin(CoveragePlugin):
    """PHPUnit Clover coverage plugin for PHP code coverage analysis."""

    def __init__(self, project_root: Optional[Path] = None, **kwargs) -> None:
        self._project_root = project_root

    @property
    def name(self) -> str:
        return "phpunit_coverage"

    @property
    def languages(self) -> List[str]:
        return ["php"]

    def get_version(self) -> str:
        return "installed"

    def ensure_binary(self) -> Path:
        # Coverage parsing doesn't need a binary - it reads XML files
        # Return a dummy path; actual parsing is from files
        import shutil

        if self._project_root:
            vendor_bin = self._project_root / "vendor" / "bin" / "phpunit"
            if vendor_bin.exists():
                return vendor_bin

        phpunit = shutil.which("phpunit")
        if phpunit:
            return Path(phpunit)

        raise FileNotFoundError(
            "PHPUnit is not installed. "
            "Install via: composer require --dev phpunit/phpunit"
        )

    def measure_coverage(
        self,
        context: ScanContext,
        threshold: float = 80.0,
    ) -> CoverageResult:
        """Parse Clover XML coverage data.

        Looks for clover XML in standard locations within the project root.

        Args:
            context: Scan context with paths and configuration.
            threshold: Coverage percentage threshold (default 80%).

        Returns:
            CoverageResult with coverage statistics.
        """
        # Find clover XML file
        clover_path = self._find_clover_file(context.project_root)
        if not clover_path:
            LOGGER.warning("No Clover XML coverage file found")
            result = CoverageResult(threshold=threshold, tool="phpunit_coverage")
            result.issues.append(self._create_no_data_issue())
            return result

        LOGGER.info(f"Parsing PHP coverage data from {clover_path.name}...")
        return self._parse_clover_xml(clover_path, context.project_root, threshold)

    def _find_clover_file(self, project_root: Path) -> Optional[Path]:
        """Find the Clover XML coverage file."""
        for rel_path in CLOVER_PATHS:
            path = project_root / rel_path
            if path.exists():
                return path
        return None

    def _parse_clover_xml(
        self, clover_path: Path, project_root: Path, threshold: float
    ) -> CoverageResult:
        """Parse Clover XML format.

        Clover XML structure:
        <coverage>
          <project>
            <package name="...">
              <file name="/path/file.php">
                <line num="10" type="stmt" count="1"/>
                <metrics loc="100" ncloc="80" statements="50" coveredstatements="40" .../>
              </file>
            </package>
            <metrics statements="500" coveredstatements="400" .../>
          </project>
        </coverage>
        """
        result = CoverageResult(threshold=threshold, tool="phpunit_coverage")

        try:
            tree = ET.parse(clover_path)
        except ET.ParseError as e:
            LOGGER.error(f"Failed to parse Clover XML: {e}")
            result.issues.append(self._create_no_data_issue())
            return result

        root: Any = tree.getroot()
        project = root.find("project")
        if project is None:
            LOGGER.warning("No <project> element in Clover XML")
            result.issues.append(self._create_no_data_issue())
            return result

        # Parse project-level metrics
        project_metrics = project.find("metrics")
        if project_metrics is not None:
            result.total_lines = int(project_metrics.get("statements", "0"))
            result.covered_lines = int(project_metrics.get("coveredstatements", "0"))
            result.missing_lines = result.total_lines - result.covered_lines

        # Parse per-file coverage
        for file_elem in project.iter("file"):
            file_name = file_elem.get("name", "")
            if not file_name:
                continue

            file_metrics = file_elem.find("metrics")
            if file_metrics is None:
                continue

            file_total = int(file_metrics.get("statements", "0"))
            file_covered = int(file_metrics.get("coveredstatements", "0"))

            # Collect missing line numbers
            missing_lines: List[int] = []
            for line_elem in file_elem.findall("line"):
                if (
                    line_elem.get("type") == "stmt"
                    and int(line_elem.get("count", "0")) == 0
                ):
                    missing_lines.append(int(line_elem.get("num", "0")))

            try:
                rel_path = str(Path(file_name).relative_to(project_root))
            except ValueError:
                rel_path = file_name

            file_coverage = FileCoverage(
                file_path=project_root / rel_path,
                total_lines=file_total,
                covered_lines=file_covered,
                missing_lines=sorted(missing_lines),
            )
            result.files[rel_path] = file_coverage

        # Generate issue if below threshold
        percentage = result.percentage
        if percentage < threshold:
            issue = self._create_coverage_issue(
                percentage, threshold, result.total_lines, result.covered_lines
            )
            result.issues.append(issue)

        LOGGER.info(
            f"PHP coverage: {percentage:.1f}% "
            f"({result.covered_lines}/{result.total_lines} statements) "
            f"- threshold: {threshold}%"
        )

        return result
