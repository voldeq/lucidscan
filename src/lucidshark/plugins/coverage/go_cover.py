"""Go coverage plugin.

Parses Go coverage data from coverprofile format (coverage.out)
generated by `go test -coverprofile=coverage.out`.
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Dict, List, Optional

from lucidshark.core.logging import get_logger
from lucidshark.core.models import ScanContext
from lucidshark.plugins.coverage.base import (
    CoveragePlugin,
    CoverageResult,
    FileCoverage,
)
from lucidshark.plugins.go_utils import find_go, get_go_version, has_go_mod

LOGGER = get_logger(__name__)


class GoCoverPlugin(CoveragePlugin):
    """Go cover plugin for Go code coverage analysis."""

    def __init__(self, project_root: Optional[Path] = None, **kwargs) -> None:
        """Initialize GoCoverPlugin.

        Args:
            project_root: Optional project root for tool resolution.
        """
        self._project_root = project_root

    @property
    def name(self) -> str:
        """Plugin identifier."""
        return "go_cover"

    @property
    def languages(self) -> List[str]:
        """Supported languages."""
        return ["go"]

    def get_version(self) -> str:
        """Get Go version."""
        return get_go_version()

    def ensure_binary(self) -> Path:
        """Ensure go is available.

        Returns:
            Path to go binary.

        Raises:
            FileNotFoundError: If go is not available.
        """
        return find_go()

    def measure_coverage(
        self,
        context: ScanContext,
        threshold: float = 80.0,
    ) -> CoverageResult:
        """Parse existing Go coverage data from coverage.out.

        Looks for coverage.out in the project root, which is generated by
        the go test runner when coverage domain is enabled.

        Args:
            context: Scan context with paths and configuration.
            threshold: Coverage percentage threshold (default 80%).

        Returns:
            CoverageResult with coverage statistics.
        """
        # Check for go.mod
        if not has_go_mod(context.project_root):
            LOGGER.info("No go.mod found, skipping go cover")
            return CoverageResult(threshold=threshold, tool="go_cover")

        # Check if coverage.out exists
        coverage_file = context.project_root / "coverage.out"
        if not coverage_file.exists():
            LOGGER.warning("No coverage.out found at %s", coverage_file)
            result = CoverageResult(threshold=threshold, tool="go_cover")
            result.issues.append(self._create_no_data_issue())
            return result

        LOGGER.info("Parsing Go coverage data from coverage.out...")

        # Read module path from go.mod for stripping from file paths
        module_path = self._read_module_path(context.project_root)

        # Parse the coverprofile
        result = self._parse_coverprofile(
            coverage_file, context.project_root, module_path, threshold
        )

        # If parsing returned empty result, add no-data issue
        if result.total_lines == 0 and not result.issues:
            result.issues.append(self._create_no_data_issue())

        return result

    def _read_module_path(self, project_root: Path) -> str:
        """Read the module path from go.mod.

        Args:
            project_root: Project root directory.

        Returns:
            Module path string (e.g., "example.com/mypackage"), or empty string.
        """
        go_mod = project_root / "go.mod"
        if not go_mod.exists():
            return ""

        try:
            content = go_mod.read_text(encoding="utf-8", errors="replace")
            for line in content.splitlines():
                line = line.strip()
                if line.startswith("module "):
                    return line[len("module ") :].strip()
        except OSError as e:
            LOGGER.warning(f"Failed to read go.mod: {e}")

        return ""

    def _parse_coverprofile(
        self,
        coverage_file: Path,
        project_root: Path,
        module_path: str,
        threshold: float,
    ) -> CoverageResult:
        """Parse Go coverprofile format.

        Format: file:startLine.startCol,endLine.endCol numStatements count

        Args:
            coverage_file: Path to coverage.out file.
            project_root: Project root directory.
            module_path: Go module path for stripping from file paths.
            threshold: Coverage percentage threshold.

        Returns:
            CoverageResult with parsed data.
        """
        result = CoverageResult(threshold=threshold, tool="go_cover")

        try:
            content = coverage_file.read_text(encoding="utf-8", errors="replace")
        except OSError as e:
            LOGGER.error(f"Failed to read coverage.out: {e}")
            return result

        # Pattern: file:startLine.startCol,endLine.endCol numStatements count
        line_pattern = re.compile(r"^(.+):(\d+)\.\d+,(\d+)\.\d+\s+(\d+)\s+(\d+)$")

        # Group data by file: file_path -> list of (start_line, end_line, num_stmts, count)
        file_blocks: Dict[str, List[tuple]] = {}

        for line in content.splitlines():
            line = line.strip()

            # Skip the mode line and empty lines
            if not line or line.startswith("mode:"):
                continue

            match = line_pattern.match(line)
            if not match:
                continue

            raw_file = match.group(1)
            start_line = int(match.group(2))
            end_line = int(match.group(3))
            num_statements = int(match.group(4))
            count = int(match.group(5))

            # Strip module path prefix to get project-relative path
            rel_file = self._strip_module_prefix(raw_file, module_path)

            if rel_file not in file_blocks:
                file_blocks[rel_file] = []
            file_blocks[rel_file].append((start_line, end_line, num_statements, count))

        # Compute per-file and overall coverage
        total_statements = 0
        total_covered = 0

        for rel_file, blocks in file_blocks.items():
            file_total = 0
            file_covered = 0
            missing_lines: List[int] = []

            for start_line, end_line, num_stmts, count in blocks:
                file_total += num_stmts
                if count > 0:
                    file_covered += num_stmts
                else:
                    missing_lines.extend(range(start_line, end_line + 1))

            total_statements += file_total
            total_covered += file_covered

            file_path = project_root / rel_file

            file_coverage = FileCoverage(
                file_path=file_path,
                total_lines=file_total,
                covered_lines=file_covered,
                missing_lines=sorted(missing_lines),
            )
            result.files[rel_file] = file_coverage

        result.total_lines = total_statements
        result.covered_lines = total_covered
        result.missing_lines = total_statements - total_covered

        # Generate issue if below threshold
        percentage = result.percentage
        if percentage < threshold:
            issue = self._create_coverage_issue(
                percentage, threshold, total_statements, total_covered
            )
            result.issues.append(issue)

        LOGGER.info(
            f"Go coverage: {percentage:.1f}% ({total_covered}/{total_statements} statements) "
            f"- threshold: {threshold}%"
        )

        return result

    def _strip_module_prefix(self, file_path: str, module_path: str) -> str:
        """Strip the Go module path prefix from a coverage file path.

        Go coverage uses full module paths like "example.com/mypackage/main.go".
        This strips the module name to get a project-relative path like "main.go".

        Args:
            file_path: Full file path from coverage data.
            module_path: Go module path from go.mod.

        Returns:
            Project-relative file path.
        """
        if module_path and file_path.startswith(module_path + "/"):
            return file_path[len(module_path) + 1 :]
        return file_path
