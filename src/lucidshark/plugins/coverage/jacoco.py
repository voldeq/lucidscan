"""JaCoCo coverage plugin.

JaCoCo is a free code coverage library for Java.
https://www.jacoco.org/jacoco/
"""

from __future__ import annotations

from pathlib import Path
from typing import List, Optional, Tuple

import defusedxml.ElementTree as ET  # type: ignore[import-untyped]

from lucidshark.core.logging import get_logger
from lucidshark.core.models import ScanContext, SkipReason, ToolDomain
from lucidshark.plugins.coverage.base import (
    CoveragePlugin,
    CoverageResult,
    FileCoverage,
)
from lucidshark.plugins.utils import find_java_build_tool

LOGGER = get_logger(__name__)


class JaCoCoPlugin(CoveragePlugin):
    """JaCoCo plugin for Java code coverage analysis."""

    def __init__(self, project_root: Optional[Path] = None):
        """Initialize JaCoCoPlugin.

        Args:
            project_root: Optional project root for finding build tools.
        """
        self._project_root = project_root

    @property
    def name(self) -> str:
        """Plugin identifier."""
        return "jacoco"

    @property
    def languages(self) -> List[str]:
        """Supported languages."""
        return ["java", "kotlin"]

    def get_version(self) -> str:
        """Get JaCoCo version from build config.

        Returns:
            Version string or 'unknown' if unable to determine.
        """
        # JaCoCo version is typically defined in pom.xml or build.gradle
        # We return a generic version indicator
        return "integrated"

    def _detect_build_system(self) -> Tuple[Path, str]:
        """Detect the build system (Maven or Gradle)."""
        project_root = self._project_root or Path.cwd()
        return find_java_build_tool(project_root)

    def ensure_binary(self) -> Path:
        """Ensure Maven or Gradle is available.

        Returns:
            Path to build tool binary.

        Raises:
            FileNotFoundError: If no build tool is found.
        """
        binary, _ = self._detect_build_system()
        return binary

    def measure_coverage(
        self,
        context: ScanContext,
        threshold: float = 80.0,
    ) -> CoverageResult:
        """Parse existing JaCoCo coverage report.

        Looks for an existing JaCoCo XML report generated by a previous test run.
        If no report is found, returns an error issue directing the user to run
        the testing domain first.

        Args:
            context: Scan context with paths and configuration.
            threshold: Coverage percentage threshold (default 80%).

        Returns:
            CoverageResult with coverage statistics and issues if below threshold.
        """
        try:
            _, build_system = self._detect_build_system()
        except FileNotFoundError as e:
            LOGGER.warning(str(e))
            context.record_skip(
                tool_name=self.name,
                domain=ToolDomain.COVERAGE,
                reason=SkipReason.MISSING_PREREQUISITE,
                message=str(e),
                suggestion="Ensure the project has a pom.xml (Maven) or build.gradle (Gradle)",
            )
            return CoverageResult(threshold=threshold, tool="jacoco")

        # Check if JaCoCo report exists
        if not self._jacoco_report_exists(context.project_root, build_system):
            LOGGER.warning("No JaCoCo report found")
            context.record_skip(
                tool_name=self.name,
                domain=ToolDomain.COVERAGE,
                reason=SkipReason.MISSING_PREREQUISITE,
                message="No JaCoCo report found",
                suggestion="Run tests with JaCoCo enabled first (mvn test or gradle test)",
            )
            result = CoverageResult(threshold=threshold, tool="jacoco")
            result.issues.append(self._create_no_data_issue())
            return result

        LOGGER.info("Using existing JaCoCo report...")

        # Parse JaCoCo report
        result = self._parse_jacoco_report(
            context.project_root, threshold, build_system, context
        )

        return result

    def _jacoco_report_exists(self, project_root: Path, build_system: str) -> bool:
        """Check if JaCoCo report exists.

        Checks root-level report paths and multi-module child directories,
        matching the same locations that _parse_jacoco_report searches.

        Args:
            project_root: Project root directory.
            build_system: Build system (maven or gradle).

        Returns:
            True if report exists.
        """
        if build_system == "maven":
            paths = [
                project_root / "target" / "site" / "jacoco" / "jacoco.xml",
                project_root / "target" / "jacoco.xml",
                project_root / "jacoco" / "jacoco.xml",
                project_root / "target" / "jacoco-report" / "jacoco.xml",
                project_root / "target" / "coverage-reports" / "jacoco.xml",
            ]
        else:
            paths = [
                project_root
                / "build"
                / "reports"
                / "jacoco"
                / "test"
                / "jacocoTestReport.xml",
                project_root / "build" / "jacoco" / "test.xml",
            ]

        if any(p.exists() for p in paths):
            return True

        # Check multi-module child directories
        for child in project_root.iterdir():
            if child.is_dir() and not child.name.startswith("."):
                if build_system == "maven":
                    if (child / "target" / "site" / "jacoco" / "jacoco.xml").exists():
                        return True
                else:
                    if (
                        child
                        / "build"
                        / "reports"
                        / "jacoco"
                        / "test"
                        / "jacocoTestReport.xml"
                    ).exists():
                        return True

        return False

    def _parse_jacoco_report(
        self,
        project_root: Path,
        threshold: float,
        build_system: str,
        context: Optional[ScanContext] = None,
    ) -> CoverageResult:
        """Parse JaCoCo XML report.

        Args:
            project_root: Project root directory.
            threshold: Coverage percentage threshold.
            build_system: Build system name (maven or gradle).
            context: Optional scan context with exclude patterns.

        Returns:
            CoverageResult with parsed data.
        """
        # Find JaCoCo XML report
        report_paths = []

        if build_system == "maven":
            report_paths = [
                project_root / "target" / "site" / "jacoco" / "jacoco.xml",
                project_root / "target" / "jacoco.xml",
                # Additional common locations used by some projects
                project_root / "jacoco" / "jacoco.xml",
                project_root / "target" / "jacoco-report" / "jacoco.xml",
                project_root / "target" / "coverage-reports" / "jacoco.xml",
            ]
        else:  # gradle
            report_paths = [
                project_root
                / "build"
                / "reports"
                / "jacoco"
                / "test"
                / "jacocoTestReport.xml",
                project_root / "build" / "jacoco" / "test.xml",
            ]

        # Check multi-module projects
        for child in project_root.iterdir():
            if child.is_dir() and not child.name.startswith("."):
                if build_system == "maven":
                    report_paths.append(
                        child / "target" / "site" / "jacoco" / "jacoco.xml"
                    )
                else:
                    report_paths.append(
                        child
                        / "build"
                        / "reports"
                        / "jacoco"
                        / "test"
                        / "jacocoTestReport.xml"
                    )

        # Find existing report
        report_file = None
        for path in report_paths:
            if path.exists():
                report_file = path
                break

        if not report_file:
            LOGGER.warning(
                "JaCoCo report not found. Ensure JaCoCo plugin is configured in your build."
            )
            return CoverageResult(threshold=threshold, tool="jacoco")

        return self._parse_xml_report(report_file, project_root, threshold, context)

    def _parse_xml_report(
        self,
        report_file: Path,
        project_root: Path,
        threshold: float,
        context: Optional[ScanContext] = None,
    ) -> CoverageResult:
        """Parse JaCoCo XML report file.

        Args:
            report_file: Path to JaCoCo XML report.
            project_root: Project root directory.
            threshold: Coverage percentage threshold.
            context: Optional scan context with exclude patterns.

        Returns:
            CoverageResult with parsed data.
        """
        try:
            tree = ET.parse(report_file)
            root = tree.getroot()
            assert root is not None
        except Exception as e:
            LOGGER.error(f"Failed to parse JaCoCo XML report: {e}")
            if context is not None:
                context.record_skip(
                    tool_name=self.name,
                    domain=ToolDomain.COVERAGE,
                    reason=SkipReason.EXECUTION_FAILED,
                    message=f"Failed to parse JaCoCo XML report: {e}",
                )
            return CoverageResult(threshold=threshold, tool="jacoco")

        # Check if we have exclude patterns from context
        has_excludes = context is not None and context.ignore_patterns is not None

        # We'll calculate totals from per-file data if we have excludes,
        # otherwise use report-level counters for efficiency
        total_lines = 0
        covered_lines = 0
        missed_lines = 0
        excluded_count = 0

        # When no excludes, use report-level LINE counter (more reliable)
        if not has_excludes:
            line_counter = root.find("counter[@type='LINE']")
            if line_counter is not None:
                missed_lines = int(line_counter.get("missed", 0))
                covered_lines = int(line_counter.get("covered", 0))
                total_lines = missed_lines + covered_lines

        result = CoverageResult(
            total_lines=total_lines,
            covered_lines=covered_lines,
            missing_lines=missed_lines,
            threshold=threshold,
            tool="jacoco",
        )

        # Parse per-package/class coverage for per-file details
        for package in root.findall(".//package"):
            package_name = package.get("name", "")

            for sourcefile in package.findall("sourcefile"):
                source_name = sourcefile.get("name", "")
                file_path = self._resolve_source_path(
                    project_root, package_name, source_name
                )

                # Check if file should be excluded
                if has_excludes:
                    assert context is not None and context.ignore_patterns is not None
                    if context.ignore_patterns.matches(file_path, project_root):
                        excluded_count += 1
                        LOGGER.debug(f"Excluding from coverage: {file_path}")
                        continue

                line_counter = sourcefile.find("counter[@type='LINE']")
                if line_counter is not None:
                    file_missed = int(line_counter.get("missed", 0))
                    file_covered = int(line_counter.get("covered", 0))

                    # Accumulate totals from non-excluded files (only when filtering)
                    if has_excludes:
                        total_lines += file_missed + file_covered
                        covered_lines += file_covered
                        missed_lines += file_missed

                    # Get missing line numbers
                    missing_line_nums = []
                    for line in sourcefile.findall("line"):
                        if int(line.get("mi", 0)) > 0:  # mi = missed instructions
                            missing_line_nums.append(int(line.get("nr", 0)))

                    file_coverage = FileCoverage(
                        file_path=file_path,
                        total_lines=file_missed + file_covered,
                        covered_lines=file_covered,
                        missing_lines=missing_line_nums,
                    )
                    # Use relative path as dictionary key (consistent with other plugins)
                    try:
                        rel_path = str(file_path.relative_to(project_root))
                    except ValueError:
                        # File outside project root - use absolute path as fallback
                        rel_path = str(file_path)
                    result.files[rel_path] = file_coverage

        # Update result with calculated totals (only needed when filtering by excludes)
        if has_excludes:
            result.total_lines = total_lines
            result.covered_lines = covered_lines
            result.missing_lines = missed_lines

        # Calculate percentage
        percentage = result.percentage

        # Generate issue if below threshold
        if percentage < threshold:
            issue = self._create_coverage_issue(
                percentage, threshold, total_lines, covered_lines
            )
            result.issues.append(issue)

        exclude_msg = (
            f" ({excluded_count} files excluded)" if excluded_count > 0 else ""
        )
        LOGGER.info(
            f"JaCoCo coverage: {percentage:.1f}% ({covered_lines}/{total_lines} lines) "
            f"- threshold: {threshold}%{exclude_msg}"
        )

        return result

    def _resolve_source_path(
        self,
        project_root: Path,
        package_name: str,
        source_name: str,
    ) -> Path:
        """Resolve source file path from package and filename.

        Args:
            project_root: Project root directory.
            package_name: Java package name (e.g., "com/example/service").
            source_name: Source file name (e.g., "MyService.java").

        Returns:
            Resolved path to source file.
        """
        # Convert package path to directory path
        relative_path = Path(package_name) / source_name

        # Check common source directories
        for src_dir in ["src/main/java", "src/test/java", "src"]:
            potential_path = project_root / src_dir / relative_path
            if potential_path.exists():
                return potential_path

        # Return best guess
        return project_root / "src" / "main" / "java" / relative_path
