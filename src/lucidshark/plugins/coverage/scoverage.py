"""Scoverage coverage plugin.

Scoverage is a code coverage tool for Scala.
https://github.com/scoverage/scalac-scoverage-plugin

Supports parsing coverage reports generated by:
- sbt-scoverage plugin
- Maven scoverage-maven-plugin
- Gradle gradle-scoverage plugin
"""

from __future__ import annotations

from pathlib import Path
from typing import List, Optional, Tuple

import defusedxml.ElementTree as ET  # type: ignore[import-untyped]

from lucidshark.core.logging import get_logger
from lucidshark.core.models import ScanContext, SkipReason, ToolDomain
from lucidshark.plugins.coverage.base import (
    CoveragePlugin,
    CoverageResult,
    FileCoverage,
)
from lucidshark.plugins.utils import find_scala_build_tool

LOGGER = get_logger(__name__)


class ScoveragePlugin(CoveragePlugin):
    """Scoverage plugin for Scala code coverage analysis."""

    def __init__(self, project_root: Optional[Path] = None, **kwargs) -> None:
        super().__init__(project_root=project_root, **kwargs)

    @property
    def name(self) -> str:
        return "scoverage"

    @property
    def languages(self) -> List[str]:
        return ["scala"]

    def get_version(self) -> str:
        return "integrated"

    def _detect_build_system(self) -> Tuple[Path, str]:
        """Detect the Scala build system."""
        project_root = self._project_root or Path.cwd()
        return find_scala_build_tool(project_root)

    def ensure_binary(self) -> Path:
        """Ensure build tool is available."""
        binary, _ = self._detect_build_system()
        return binary

    def measure_coverage(
        self,
        context: ScanContext,
        threshold: float = 80.0,
    ) -> CoverageResult:
        """Parse existing Scoverage report.

        Looks for an existing Scoverage XML report generated by a previous
        test run. If no report is found, returns an error issue directing
        the user to run the testing domain first.

        Args:
            context: Scan context with paths and configuration.
            threshold: Coverage percentage threshold (default 80%).

        Returns:
            CoverageResult with coverage statistics.
        """
        try:
            _, build_system = self._detect_build_system()
        except FileNotFoundError as e:
            LOGGER.warning(str(e))
            context.record_skip(
                tool_name=self.name,
                domain=ToolDomain.COVERAGE,
                reason=SkipReason.MISSING_PREREQUISITE,
                message=str(e),
                suggestion="Ensure the project has build.sbt, pom.xml, or build.gradle",
            )
            return CoverageResult(threshold=threshold, tool="scoverage")

        # Find Scoverage report
        report_file = self._find_scoverage_report(context.project_root, build_system)
        if not report_file:
            LOGGER.warning("No Scoverage report found")
            context.record_skip(
                tool_name=self.name,
                domain=ToolDomain.COVERAGE,
                reason=SkipReason.MISSING_PREREQUISITE,
                message="No Scoverage report found",
                suggestion=(
                    "Run tests with Scoverage enabled first "
                    "(sbt coverage test coverageReport)"
                ),
            )
            result = CoverageResult(threshold=threshold, tool="scoverage")
            result.issues.append(self._create_no_data_issue())
            return result

        LOGGER.info(f"Using existing Scoverage report: {report_file}")
        return self._parse_scoverage_report(report_file, context.project_root, threshold, context)

    def _find_scoverage_report(
        self, project_root: Path, build_system: str
    ) -> Optional[Path]:
        """Find Scoverage XML report file.

        Args:
            project_root: Project root directory.
            build_system: Build system (sbt, maven, or gradle).

        Returns:
            Path to the report file, or None.
        """
        report_paths = []

        if build_system == "sbt":
            # sbt-scoverage report locations
            target_dir = project_root / "target"
            if target_dir.exists():
                for child in target_dir.iterdir():
                    if child.is_dir() and child.name.startswith("scala-"):
                        report_paths.append(
                            child / "scoverage-report" / "scoverage.xml"
                        )
                        report_paths.append(
                            child / "coverage-report" / "cobertura.xml"
                        )

            report_paths.extend([
                project_root / "target" / "scoverage-report" / "scoverage.xml",
                project_root / "target" / "scala-2.13" / "scoverage-report" / "scoverage.xml",
                project_root / "target" / "scala-3" / "scoverage-report" / "scoverage.xml",
            ])

        elif build_system == "maven":
            report_paths.extend([
                project_root / "target" / "scoverage.xml",
                project_root / "target" / "site" / "scoverage" / "scoverage.xml",
            ])

        else:  # gradle
            report_paths.extend([
                project_root / "build" / "reports" / "scoverage" / "scoverage.xml",
            ])

        for path in report_paths:
            if path.exists():
                return path

        return None

    def _parse_scoverage_report(
        self,
        report_file: Path,
        project_root: Path,
        threshold: float,
        context: Optional[ScanContext] = None,
    ) -> CoverageResult:
        """Parse Scoverage XML report.

        Scoverage XML format contains:
        <scoverage statement-count="N" statements-invoked="M" ...>
          <packages>
            <package name="com.example">
              <classes>
                <class name="MyClass" filename="..." statement-count="..." ...>
                  <methods>...</methods>
                </class>
              </classes>
            </package>
          </packages>
        </scoverage>
        """
        try:
            tree = ET.parse(report_file)
            root = tree.getroot()
            assert root is not None
        except Exception as e:
            LOGGER.error(f"Failed to parse Scoverage XML report: {e}")
            if context is not None:
                context.record_skip(
                    tool_name=self.name,
                    domain=ToolDomain.COVERAGE,
                    reason=SkipReason.EXECUTION_FAILED,
                    message=f"Failed to parse Scoverage XML report: {e}",
                )
            return CoverageResult(threshold=threshold, tool="scoverage")

        # Check if we have exclude patterns from context
        has_excludes = context is not None and context.ignore_patterns is not None

        # Extract totals from root element
        total_statements = int(root.get("statement-count", 0))
        invoked_statements = int(root.get("statements-invoked", 0))
        statement_rate = float(root.get("statement-rate", 0))

        result = CoverageResult(
            total_lines=total_statements,
            covered_lines=invoked_statements,
            missing_lines=total_statements - invoked_statements,
            threshold=threshold,
            tool="scoverage",
        )

        # Parse per-file coverage from class elements
        excluded_count = 0
        if has_excludes:
            # Recalculate from non-excluded files
            result.total_lines = 0
            result.covered_lines = 0
            result.missing_lines = 0

        for package in root.findall(".//package"):
            for cls in package.findall(".//class"):
                filename = cls.get("filename", "")
                if not filename:
                    continue

                file_path = self._resolve_source_path(project_root, filename)

                # Check if file should be excluded
                if has_excludes:
                    assert context is not None and context.ignore_patterns is not None
                    if context.ignore_patterns.matches(file_path, project_root):
                        excluded_count += 1
                        continue

                cls_total = int(cls.get("statement-count", 0))
                cls_invoked = int(cls.get("statements-invoked", 0))

                if has_excludes:
                    result.total_lines += cls_total
                    result.covered_lines += cls_invoked
                    result.missing_lines += cls_total - cls_invoked

                try:
                    rel_path = str(file_path.relative_to(project_root))
                except ValueError:
                    rel_path = str(file_path)

                file_coverage = FileCoverage(
                    file_path=file_path,
                    total_lines=cls_total,
                    covered_lines=cls_invoked,
                    missing_lines=[],
                )
                result.files[rel_path] = file_coverage

        percentage = result.percentage

        if percentage < threshold:
            issue = self._create_coverage_issue(
                percentage, threshold, result.total_lines, result.covered_lines
            )
            result.issues.append(issue)

        exclude_msg = (
            f" ({excluded_count} files excluded)" if excluded_count > 0 else ""
        )
        LOGGER.info(
            f"Scoverage coverage: {percentage:.1f}% "
            f"({result.covered_lines}/{result.total_lines} statements) "
            f"- threshold: {threshold}%{exclude_msg}"
        )

        return result

    def _resolve_source_path(
        self,
        project_root: Path,
        filename: str,
    ) -> Path:
        """Resolve source file path from Scoverage filename.

        Args:
            project_root: Project root directory.
            filename: Source file path from Scoverage report.

        Returns:
            Resolved path to source file.
        """
        path = Path(filename)

        # If already absolute, check existence
        if path.is_absolute():
            if path.exists():
                return path
            # Try to make it relative to project root
            try:
                path = path.relative_to(project_root)
            except ValueError:
                return path

        # Check common source directories
        for src_dir in ["src/main/scala", "src/test/scala", "src"]:
            potential_path = project_root / src_dir / path
            if potential_path.exists():
                return potential_path

        # Check directly from project root
        direct_path = project_root / path
        if direct_path.exists():
            return direct_path

        # Return best guess
        return project_root / "src" / "main" / "scala" / path
